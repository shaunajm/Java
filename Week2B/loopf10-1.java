//Write a program which will print out the numbers 10 down to 1 inclusive using a for loop.
public class TenToOne
{
    public static void main(String [] args)
    {
        // Use a for loop to print out the numbers.
        int i;
        
        for(i = 10; i > 0; i--){
            System.out.print(i + " ");
        }
        
        System.out.println();
    }
}
