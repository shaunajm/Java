//Write a program which will print out the numbers 5 down to 20 inclusive using a for loop.
public class FiveToTwenty
{
    public static void main(String [] args)
    {
        // Use a for loop to print out the numbers.
        int i;
        
        for(i = 5; i <= 20; i++){
            System.out.print(i + " ");
        }
        
        System.out.println();
    }
}
