//Write a program which will print out the numbers 1 to 10 inclusive using a for loop.
public class OneToTen
{
    public static void main(String [] args)
    {
        // Use a for loop to print out the numbers.
        int i;
        
        for(i = 1; i <= 10; i++){
            System.out.print(i + " ");
        }
        
        System.out.println();
    }
}
