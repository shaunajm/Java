//Write a program which will print out the numbers 1 to 10 inclusive using a while loop.
public class OneToTen
{
    public static void main(String [] args)
    {
        int i;
        i = 1;
        
        while(i <= 10){
            System.out.print(i + " ");
            i++;
        }
       
        System.out.println(); // Finish with a new line.
    }
}
